# AeroFrame-DT FreeCAD parameter-sheet macro
import FreeCAD as App
doc = App.ActiveDocument or App.newDocument('AeroFrame_DT')
sheet = doc.getObject('Parameters') or doc.addObject('Spreadsheet::Sheet', 'Parameters')
sheet.set('A2', 'lug_width')
sheet.set('B2', '100.0 mm')
sheet.setAlias('B2', 'lug_width')
sheet.set('C2', 'Representative lug net-section width')
sheet.set('A3', 'hole_diameter')
sheet.set('B3', '40.0 mm')
sheet.setAlias('B3', 'hole_diameter')
sheet.set('C3', 'Representative pin-hole diameter')
sheet.set('A4', 'lug_thickness')
sheet.set('B4', '20.0 mm')
sheet.setAlias('B4', 'lug_thickness')
sheet.set('C4', 'Representative lug thickness')
sheet.set('A5', 'edge_distance')
sheet.set('B5', '55.0 mm')
sheet.setAlias('B5', 'edge_distance')
sheet.set('C5', 'Hole center to free edge')
sheet.set('A6', 'flange_thickness')
sheet.set('B6', '16.0 mm')
sheet.setAlias('B6', 'flange_thickness')
sheet.set('C6', 'Representative flange thickness')
sheet.set('A7', 'blend_radius')
sheet.set('B7', '12.0 mm')
sheet.setAlias('B7', 'blend_radius')
sheet.set('C7', 'Representative transition radius')
doc.recompute()
doc.saveAs('AeroFrame_DT_parameters.FCStd')
